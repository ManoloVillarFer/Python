# Programa que pida dos números e indique si el primero es mayor que el segundo o no.

# Pedimos los números
n1 = int(input("Dame el primer número: "))
n2 = int(input("Dame el segundo número: "))

# Calculos y de nuevo datos
if n1 > n2:
    print("Es mayor")
else:
    print("Es menor")
    