# 4. Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

n1 = float(input("Dame el primer número "))
n2 = float(input("Dame el segundo número "))

suma = n1+n2
resta = n1-n2
multiplicacion = n1*n2
division = n1/n2

print("El resultado de la suma es", suma, "El resultado de la resta es", resta,
      "El resultado de la multiplicación es", multiplicacion, "El resultado de la división es", division)
